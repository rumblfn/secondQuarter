"use strict";

function addition(num1, num2) {
    return num1 + num2;
}

function subtraction(num1, num2) {
    return num1 - num2;
}

function division(num1, num2) {
    return num1 / num2;
}

function multiplication(num1, num2) {
    return num1 * num2;
}

// проверка
alert(addition(3, 5));
alert(subtraction(10, 8));
alert(division(16, 4));
alert(multiplication(7, 9));